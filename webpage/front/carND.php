        <h4>Behavioral cloning</h4>
        <p>This project was probably inspired by Nvidia's article <a href="https://arxiv.org/abs/1604.07316#">'End to End Learning for Self-Driving Cars'</a>. Using Keras (a python framework that utilizes Tensorflow) I trained a neural network to mimic my driving. The dataset was collected by me by recording manual driving in the simulator provided by Udacity. The network took an image from a forward looking camera as input and gave a steering angle as output.</p>

        <p>Keywords: Keras, neural networks, supervised learning, regression</p>
        <a href="http://svene.se/carND/behavioralCloningEasy2.mp4">Movie link</a><br>
        <a href="https://github.com/sven91swe/CarND-BehavioralCloning">Github link</a>
