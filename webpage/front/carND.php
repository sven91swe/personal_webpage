        <h2>Udacity: Self-driving car nanodegree</h2>
        <p>When I started working at NEVS one of my collegues recommended a free online course in software development from Udacity. After completing that free course I noticed the Self-driving car nanodegree program. I signed up as it covered topics that is both interesting to me and useful for myself and my employer. Using my spare time and my resources I have during the program learned a lot and some of it I use in my daily work.</p>

        <p>I should probably point out that a few of these solutions could be improved upon. For me it was a learning experience and I could both have documented the code better and tuned it for better performance. But I focused on learning the concepts instead.</p>

        <p>In addition to the descriptions on this site there is also a writeup that was written as part of the handin to Udacity for some of the projects. If a writeup exists for that project, it is linked from the github readme file.</p>
