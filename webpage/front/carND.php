        <h4>Traffic sign Classifier</h4>
        <p>In this project I classified traffic signs based on color images (32x32 pixels) using a neural network. The data used was a labaled data set of images provided by Udacity. My solution was based on the LeNet architecture which I implement using Tensorflow. This was modified by me and trained using parts of the data set. The trained network achived ~95% correct guesses on the test set.</p>

        <p>Keywords: Tensorflow, neural networks, supervised learning, classification</p>
        <a href="https://github.com/sven91swe/CarND-TrafficSigns">Github link</a>
    </div>
